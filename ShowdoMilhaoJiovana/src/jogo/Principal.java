    private void btnplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnplayActionPerformed
        // TODO add your handling code here:
        if(txtlogin.getText().isEmpty() || txtsenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"Por favor, preencha todos os campos.");
        
        }else{
            Jogador jogador = new Jogador();
            jogador.setLogin(txtlogin.getText());
            jogador.setSenha(txtsenha.getText());
            JogadorDAO dao = new JogadorDAO();
            
            Jogador retorno = dao.login(jogador);
            if(retorno == null){
                JOptionPane.showMessageDialog(rootPane,"Login e/ou senha invalidos");
            }else{
                Jogo tela = new Jogo();
                tela.setJogador(retorno);
                tela.setVisible(true);
                //fecha teela atual
                this.setVisible(false);
                //parar som
                audio.parar();
            }
        }
    }//GEN-LAST:event_btnplayActionPerformed

